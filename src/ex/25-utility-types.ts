// ======== Utility Types =========
// Utility Types 可以幫助我們操作和轉換型別。
// 以下是一些常用的 Utility Types：

// Partial<T>：將型別 T 的所有屬性變為可選。



// Required<T>：將型別 T 的所有屬性變為必填。



// Readonly<T>：將型別 T 的所有屬性變為只讀。

// Record<K, T>：創建一個包含屬性鍵為 K，屬性值為 T 的物件型別。

// Pick<T, K>：從型別 T 中選擇指定的屬性鍵 K。

// Omit<T, K>：從型別 T 中排除指定的屬性鍵 K。

// Exclude<T, U>：從型別 T 中排除可以賦值給 U 的型別。

// Extract<T, U>：從型別 T 中提取可以賦值給 U 的型別。

// NonNullable<T>：從型別 T 中排除 null 和 undefined。

// ReturnType<T>：獲取函數類型 T 的返回值型別。